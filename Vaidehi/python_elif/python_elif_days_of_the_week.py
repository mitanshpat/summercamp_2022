operant = input("Enter a day of the week: ")

if operant == "Monday":
    print ("Tuesday, Wednesday, Thursday, Friday, Saturday, Sunday")
elif operant == "Tuesday":
    print ("Wednesday, Thursday, Friday, Saturday, Sunday")
elif operant == "Wednesday":
    print ("Thursday, Friday, Saturday, Sunday")
elif operant == "Thursday":
    print ("Friday, Saturday, Sunday")
elif operant == "Friday":
    print ("Saturday, Sunday")
elif operant == "Saturday":
    print("Sunday")
elif operant == "Sunday":
    print("")