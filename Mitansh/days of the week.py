user_input = input('Enter the day of the week: ')
if user_input == 'Sunday':
    print('')
if user_input == 'Monday':
    print('Tuesday, Wednesday, Thursday, Friday, Saturday, Sunday')
if user_input == 'Tuesday':
    print('Wednesday, Thursday, Friday, Saturday, Sunday')
if user_input == 'Wednesday':
    print('Thursday, Friday, Saturday, Sunday')
if user_input == 'Thursday':
    print('Friday, Saturday, Sunday')
if user_input == 'Friday':
    print('Saturday, Sunday')
if user_input == 'Saturday':
    print('Sunday')

